package com.example.android_firebase_2.models;

public class User {
    public String name;
    public String email;
    public String phone;
    public String address;

    public User() { }

    public User(String name, String email, String phone, String address) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }
}

